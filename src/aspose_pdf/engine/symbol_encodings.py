"""Built-in encodings of the symbolic Standard-14 fonts and StandardEncoding.

``SYMBOL_TO_UNICODE`` / ``ZAPF_DINGBATS_TO_UNICODE`` map the single-byte codes
of the Adobe Symbol and ITC ZapfDingbats built-in encodings to Unicode, so the
renderer and text pipeline can resolve glyphs for these fonts (which are never
embedded and do not use a Latin encoding). Generated from the Adobe mapping
tables published by the Unicode Consortium
(``unicode.org/Public/MAPPINGS/VENDORS/ADOBE/{symbol,zdingbat}.txt``), with a
few private-use/deprecated codepoints re-aliased to their canonical Unicode
equivalents (identical shapes, present in common substitute fonts):

* Symbol ``0xE1``/``0xF1`` (U+2329/U+232A, deprecated) -> U+27E8/U+27E9;
* Symbol serif/sans copyright, registered and trademark variants
  (U+F6D9-F6DB, U+F8E8-F8EA) -> U+00A9/U+00AE/U+2122;
* ZapfDingbats ornament brackets ``0x80``-``0x8D`` (U+F8D7-F8E4) ->
  U+2768-U+2775 (added to Unicode after the Adobe table was published).

The remaining private-use codes (Symbol's radical/arrow extenders and
big-delimiter pieces, U+F8E5-F8FE) have no canonical equivalent and degrade to
the renderer's glyph-box fallback.

``STANDARD_ENCODING_NAMES`` maps Adobe StandardEncoding codes to glyph names,
used by the Type 1 ``seac`` accent-composition operator (whose base/accent
character codes are defined in terms of StandardEncoding).
"""

