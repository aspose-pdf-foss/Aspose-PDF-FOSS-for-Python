def test_get_attach_names_no_duplicate_def():
    """get_attach_names must be defined exactly once in SimplePdf."""
    src_file = inspect.getsourcefile(SimplePdf)
    assert src_file is not None
    with open(src_file, "r", encoding="utf-8") as f:
        tree = ast.parse(f.read())
    class_node = None
    for node in tree.body:
        if isinstance(node, ast.ClassDef) and node.name == "SimplePdf":
            class_node = node
            break
    assert class_node is not None
    count = sum(
        1
        for n in class_node.body
        if isinstance(n, ast.FunctionDef) and n.name == "get_attach_names"
    )
    assert count == 1


