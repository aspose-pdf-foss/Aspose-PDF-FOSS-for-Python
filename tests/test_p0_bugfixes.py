def test_save_image_writes_file(tmp_path):
    pdf = SimplePdf()
    pdf.images["img1"] = b"PNG_DATA"
    out_file = tmp_path / "out.png"
    pdf.save_image("img1", out_file)
    assert out_file.is_file()
    assert out_file.read_bytes() == b"PNG_DATA"


def test_save_image_missing_name_raises(tmp_path):
    pdf = SimplePdf()
    out_file = tmp_path / "out.png"
    with pytest.raises(KeyError):
        pdf.save_image("nonexistent", out_file)


def test_save_image_after_dispose_raises(tmp_path):
    pdf = SimplePdf()
    pdf.images["img1"] = b"PNG_DATA"
    pdf.dispose()
    out_file = tmp_path / "out.png"
    with pytest.raises(Exception):
        pdf.save_image("img1", out_file)
